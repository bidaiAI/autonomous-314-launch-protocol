export type Locale = "en" | "zh";
const STORAGE_KEY = "a314_locale";

let currentLocale: Locale = (localStorage.getItem(STORAGE_KEY) as Locale) ?? "en";
const listeners = new Set<() => void>();

export function getLocale(): Locale {
  return currentLocale;
}

export function setLocale(locale: Locale) {
  currentLocale = locale;
  localStorage.setItem(STORAGE_KEY, locale);
  listeners.forEach((fn) => fn());
}

export function toggleLocale() {
  setLocale(currentLocale === "en" ? "zh" : "en");
}

export function onLocaleChange(fn: () => void) {
  listeners.add(fn);
  return () => listeners.delete(fn);
}

// eslint-disable-next-line @typescript-eslint/no-explicit-any
function getTable(): any { return currentLocale === "zh" ? strings.zh : strings.en; }
// eslint-disable-next-line @typescript-eslint/no-explicit-any
function getFallback(): any { return strings.en; }

export function t(key: string): string {
  const val = getTable()[key] ?? getFallback()[key] ?? key;
  return typeof val === "string" ? val : (val as string[])[0] ?? key;
}

export function tf(key: string, vars: Record<string, string | number>): string {
  return t(key).replace(/\{(\w+)\}/g, (_, name) => String(vars[name] ?? `{${name}}`));
}

export function ta(key: string): string[] {
  const val = getTable()[key] ?? getFallback()[key];
  return Array.isArray(val) ? [...val] : [];
}

